/*
*****************************************************************************
** 函 数 名 ：void Lcd12232_MoveToXY(uint8 x, uint8 y)  
** 功    能 ：将光标移到指点位置 
** 说    明 ：第一行地址为80H~87H 第二行地址90H~97H
** 入口参数 ：x代表行，y代表列
** 返 回 值 ：无
** 说明     ：
*****************************************************************************/
void LCD_MoveToXY(u8 x, u8 y)  
{
	switch(x)
	{
		case 0:
			LCD_WriteCom(0x80 + y);
			break;
		case 1 :
			LCD_WriteCom(0x90 + y);
			break;
		case 2 :
			LCD_WriteCom(0x88 + y);
			break;
		case 3 :
			LCD_WriteCom(0x98 + y);
			break;
		default :
			break;
	}
}
